const ExploreCareer = () => {
  const navigate = useNavigate();
  const [loading, setLoading] = useState(true);
  const [careers, setCareers] = useState([]);
  const [filteredCareers, setFilteredCareers] = useState([]);
  const [searchTerm, setSearchTerm] = useState('');
  const [selectedCategory, setSelectedCategory] = useState('All');

  useEffect(() => {
    // In a real app, you would fetch data from an API
    const fetchCareers = async () => {
      try {
        // Simulate API call delay
        setTimeout(() => {
          setCareers(sampleCareers);
          setFilteredCareers(sampleCareers);
          setLoading(false);
        }, 1000);
      } catch (error) {
        console.error('Error fetching careers:', error);
        setLoading(false);
      }
    };

    fetchCareers();
  }, []);

  useEffect(() => {
    // Filter careers based on search term and category
    const filtered = careers.filter((career) => {
      const matchesSearch = career.title.toLowerCase().includes(searchTerm.toLowerCase()) ||
                           career.description.toLowerCase().includes(searchTerm.toLowerCase());
      
      const matchesCategory = selectedCategory === 'All' || career.category === selectedCategory;
      
      return matchesSearch && matchesCategory;
    });
    
    setFilteredCareers(filtered);
  }, [searchTerm, selectedCategory, careers]);

  const handleSearchChange = (event) => {
    setSearchTerm(event.target.value);
  };

  const handleCategorySelect = (category) => {
    setSelectedCategory(category);
  };

  const handleCareerSelect = (careerId) => {
    navigate(`/career/${careerId}`);
  };

  // Create categories array safely with null check
  const categories = ['All', ...new Set((careers || []).map(career => career.category))];

  if (loading) {
    return (
      <Container maxWidth="lg">
        <Box sx={{ display: 'flex', justifyContent: 'center', my: 8 }}>
          <CircularProgress />
        </Box>
      </Container>
    );
  }

  return (
    <Container maxWidth="lg">
      <Paper elevation={0} sx={{ p: 3, mb: 4 }}>
        <Typography variant="h4" gutterBottom>
          Explore Careers
        </Typography>
        <Typography variant="body1" color="text.secondary" paragraph>
          Browse through various career options, their requirements, and growth prospects in India.
        </Typography>

        <TextField
          fullWidth
          variant="outlined"
          placeholder="Search for careers..."
          value={searchTerm}
          onChange={handleSearchChange}
          sx={{ mb: 3 }}
          InputProps={{
            startAdornment: (
              <InputAdornment position="start">
                <SearchIcon />
              </InputAdornment>
            ),
          }}
        />

        <Box sx={{ mb: 3, display: 'flex', flexWrap: 'wrap', gap: 1 }}>
          {categories && categories.map((category) => (
            <Chip
              key={category}
              label={category}
              onClick={() => handleCategorySelect(category)}
              color={selectedCategory === category ? 'primary' : 'default'}
              variant={selectedCategory === category ? 'filled' : 'outlined'}
              sx={{ m: 0.5 }}
            />
          ))}
        </Box>
      </Paper>

      {filteredCareers.length === 0 ? (
        <Paper elevation={1} sx={{ p: 3, textAlign: 'center' }}>
          <Typography variant="h6" color="text.secondary">
            No careers found matching your criteria.
          </Typography>
        </Paper>
      ) : (
        <Grid container spacing={3}>
          {filteredCareers.map((career) => (
            <Grid item key={career.id} xs={12} sm={6} md={4}>
              <Card 
                sx={{ 
                  height: '100%', 
                  display: 'flex', 
                  flexDirection: 'column',
                  transition: '0.3s',
                  '&:hover': {
                    transform: 'translateY(-5px)',
                    boxShadow: '0 8px 16px rgba(0,0,0,0.1)',
                  },
                }}
              >
                <CardContent sx={{ flexGrow: 1 }}>
                  <Typography gutterBottom variant="h5" component="h2">
                    {career.title}
                  </Typography>
                  <Chip 
                    label={career.category} 
                    size="small" 
                    color="primary" 
                    variant="outlined" 
                    sx={{ mb: 2 }} 
                  />
                  <Typography variant="body2" color="text.secondary" paragraph>
                    {career.description}
                  </Typography>
                  
                  <Box sx={{ display: 'flex', alignItems: 'center', mb: 1 }}>
                    <SchoolIcon fontSize="small" color="action" sx={{ mr: 1 }} />
                    <Typography variant="body2">
                      {career.education}
                    </Typography>
                  </Box>
                  
                  <Box sx={{ display: 'flex', alignItems: 'center', mb: 1 }}>
                    <WorkIcon fontSize="small" color="action" sx={{ mr: 1 }} />
                    <Typography variant="body2">
                      {career.salary}
                    </Typography>
                  </Box>
                  
                  <Box sx={{ display: 'flex', alignItems: 'center' }}>
                    <TrendingUpIcon 
                      fontSize="small" 
                      color={career.growth === 'High' ? 'success' : 'action'} 
                      sx={{ mr: 1 }} 
                    />
                    <Typography 
                      variant="body2" 
                      color={career.growth === 'High' ? 'success.main' : 'text.primary'}
                    >
                      Growth: {career.growth}
                    </Typography>
                  </Box>
                  
                  <Divider sx={{ my: 2 }} />
                  
                  <Typography variant="subtitle2" gutterBottom>
                    Key Skills:
                  </Typography>
                  <Box sx={{ display: 'flex', flexWrap: 'wrap', gap: 0.5 }}>
                    {career.skills.map((skill) => (
                      <Chip 
                        key={skill} 
                        label={skill} 
                        size="small" 
                        variant="outlined" 
                        sx={{ m: 0.5 }} 
                      />
                    ))}
                  </Box>
                </CardContent>
                <CardActions>
                  <Button 
                    size="small" 
                    fullWidth 
                    variant="contained" 
                    onClick={() => handleCareerSelect(career.id)}
                  >
                    View Details
                  </Button>
                </CardActions>
              </Card>
            </Grid>
          ))}
        </Grid>
      )}
    </Container>
  );
};

export default ExploreCareer;